export default function groceriesList() {
  /**
    * Returns a Map object with the name and quantity of groceries.
    */
  const myMap = new Map();
  myMap.set('Apples', 10);
  myMap.set('Tomatoes', 10);
  myMap.set('Pasta', 1);
  myMap.set('Rice', 1);
  myMap.set('Banana', 5);
  return myMap;
}
  